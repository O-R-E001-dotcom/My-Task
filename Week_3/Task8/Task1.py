# # Explaining each output
num1 = int(input("Enter 1st number: "))     # 3
num2 = int(input("Enter 2nd number: "))     # 5
print(f"{num1} == {num2} : {num1 == num2}") # 3 == 5 : False(bcus 3 is not equals to 5)
print(f"{num1} != {num2} : {num1 != num2}") # 3 != 5 : True (because the sign != is already telling us that it is not equals to i.e 3 is not equals to 5)
print(f"{num1} > {num2} : {num1 > num2}") # 3 > 5 : False (bcuz 3 is not greater than 5, it is less than 5)
print(f"{num1} < {num2} : {num1 < num2}") # 3 < 5 : True (bcuz 3 is less than 5, 5 is greater than 3)

